        $response = $client->request('GET', 'api/reg');
        $regions = json_decode($response->getBody());
        return view('admin.dis.create',[
            'regions' => $regions->data
        ]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $client = new Client(['base_uri' => 'http://certificate.loc/']);
        $request = $request->except('_token');
        $response = $client->request('POST','api/dis',['form_params' => $request, 'headers' =>$this->headers]);
        if($response ==true)
        {
            return redirect()->route('dis.index');
        }
    }

